function fillLAnguages(){
   $.each(languages.languages, function (i, item) {
      if(item.language_code == 'en-US'){
         $('<option>')
            .val(item.language_code)
            .attr('selected','selected')
            .text(item.language_name)
            .appendTo('#language');
      }else{
         $('<option>')
            .val(item.language_code)
            .text(item.language_name)
            .appendTo('#language');
      }
  });
}

function changeLanguage(){
   var lang = $("#language").val();
   $('#voice').find('option').remove()
   $.each(voices.voices, function (i, item) {
      if(item.language_code == lang){
         $("#voice").append(new Option(item.voice_code, item.voice_code));
      }
   });
}


$(document).ready(function(){
   fillLAnguages();
   changeLanguage();
   
   $("#language").change(function(){
      changeLanguage();
   })

   $("#submit").click(function(){
      var voice = $("#voice").val();
      var text  = $("#text").val();
      if(text.trim() == ""){
         showError("text is required")
      }else{
         $.ajax({
            type: 'post',
            url: 'synthesize.php',
            data: {
               voice: voice,
               text: text
            },
            dataType: 'json',
            beforeSend: function(){
               disableForm();
               $("#result").html("");
            },
            success: function (data) {
               if(data.status == "success"){
                  var html = '<audio controls autoplay>'
                  html += '<source src="data:audio/mpeg;base64,'+data.audio_base64+'" type="audio/mpeg">'
                  html += 'Your browser does not support the audio element.'
                  html += '</audio>'
                  $("#result").html(html)
               }else{
                  alert(data.message);
               }
               enableForm();
            },
            error: function(){
               alert("Failed to connect to server");
               enableForm();
            }
         });
      }
   })
})


function disableForm(){
   $("form *").attr("disabled", "disabled");
}

function enableForm(){
   $("form *").removeAttr("disabled");
}